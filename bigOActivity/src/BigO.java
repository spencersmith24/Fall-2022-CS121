public class BigO {


    public void printNSquaredTimes(int n) {
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= n; j++) {
                System.out.printf("\nHi hello :)");
            }
        }
    }

    public void printNtimes(int n){
        for(int i = 1; i <= n; i++){
            System.out.printf("\nThis has been printed %d times.", i);
        }
    }
}
